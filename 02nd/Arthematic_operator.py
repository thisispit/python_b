""" Arthematic operators  """


m=100
n=10
print("Arthematic operator are: ")
print("a+b=", m+n)  #add
print("a-b=", m-n)  #sub
print("a*b=", m*n)  #mult
print("a/b=", m/n)  #Div
print("a**b=", m**n)#Power A power B


""" Relational (Comperasion) operators """

y=5
x=10
print("Rational operator: ")
print("x==y:", x==y) #equal to
print("x!=y:", x!=y) #not equal to
print("x>y:", x>y) #greater than
print("x<y:", x<y) #less than
print("x>=y:", x>=y) #greater than equal to
print("x<=y:", x<=y) #less than equal to



